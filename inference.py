logger = logging.getLogger(__name__)

# ---------------------------
# 2. HELPER FUNCTIONS (from training script)
# ---------------------------
def fix_length(audio, target_len):
    """Pad or truncate audio to target length."""
    if len(audio) < target_len:
        return np.pad(audio, (0, target_len - len(audio)), mode='constant')
    else:
        return audio[:target_len]

def normalise_mel(mel):
    """Min-max normalize mel across time & freq."""
    mel = mel.astype(np.float32)
    mel = (mel - mel.min()) / (mel.max() - mel.min() + 1e-8)
    return mel

def extract_mel(wav_path, sr=SAMPLE_RATE):
    """Extract mel spectrogram from .wav file."""
    y, _ = librosa.load(wav_path, sr=sr, mono=True)
    y = fix_length(y, int(DURATION * sr))
    mel = librosa.feature.melspectrogram(
        y=y,
        sr=sr,
        n_fft=N_FFT,
        hop_length=HOP_LENGTH,
        n_mels=N_MELS,
        fmax=FMAX
    )
    mel = librosa.power_to_db(mel, ref=np.max)
    mel = normalise_mel(mel)
    return mel  # (n_mels, T)

# ---------------------------
# 3. DATASET CLASS (for inference - no augmentation)
# ---------------------------
class InferenceDataset(Dataset):
    def __init__(self, df):
        self.df = df.reset_index(drop=True)

    def __len__(self):
        return len(self.df)

    def __getitem__(self, idx):
        row = self.df.iloc[idx]
        mix_path = row["mix_path"] # Assuming the CSV has mix_path column
        label = row["label"] # Assuming the CSV has label column (integer)

        # Load and extract mel
        mel = extract_mel(mix_path)
        mel = torch.from_numpy(mel).float()  # (128, T)

        # Ensure fixed shape
        if mel.shape[1] < TARGET_FRAMES:
            pad = torch.zeros(128, TARGET_FRAMES - mel.shape[1])
            mel = torch.cat([mel, pad], dim=1)
        else:
            mel = mel[:, :TARGET_FRAMES]

        # Return mel, integer label, and the original file_base for identification if needed
        return {"mix": mel.unsqueeze(0), "label": torch.tensor(label, dtype=torch.long), "file_base": row["file_base"]} # (1, 128, T)

