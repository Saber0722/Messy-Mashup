# Load label encoder
with open(LABEL_ENCODER_PATH, "rb") as f:
    le: LabelEncoder = pickle.load(f)
idx2label = {i: l for i, l in enumerate(le.classes_)}
logger.info(f"Loaded label encoder for classes: {list(le.classes_)}")

# Load test dataframe (it should contain 'mix_path' and 'label' columns)
test_df = pd.read_csv(TEST_CSV_PATH)
# The test_df generated by our script has 'genre', 'track_id', 'mix_path', 'file_base', 'label'
logger.info(f"Loaded test dataframe with {len(test_df)} samples.")

# Load model
from fresh_training_from_scratch import GenreClassifier # Import the model class from the training script

num_classes = len(le.classes_)
model = GenreClassifier(num_classes=num_classes).to(DEVICE)

checkpoint = torch.load(MODEL_PATH, map_location=DEVICE, weights_only=False)
model.load_state_dict(checkpoint['model_state_dict'])
model.eval() # Set to eval mode for inference
logger.info("Model loaded and set to eval mode.")

# ---------------------------
# 5. RUN INFERENCE ON TEST SET
# ---------------------------
test_dataset = InferenceDataset(test_df)
test_loader = DataLoader(test_dataset, batch_size=32, shuffle=False, num_workers=4) # Batch size can be larger for inference

all_preds = []
all_true = []
all_file_bases = [] # Store file bases if needed for debugging

logger.info("Starting inference on test set...")
with torch.no_grad(): # Disable gradient computation for efficiency
    for batch in test_loader:
        x = batch["mix"].to(DEVICE)
        y_true = batch["label"].to(DEVICE)
        file_bases = batch["file_base"] # List of strings

        logits = model(x)
        preds = torch.argmax(logits, dim=1) # Get predicted class indices

        all_preds.extend(preds.cpu().numpy())
        all_true.extend(y_true.cpu().numpy())
        all_file_bases.extend(file_bases) # Extend list of strings

logger.info("Inference completed.")

# ---------------------------
# 6. CALCULATE AND PRINT METRICS
# ---------------------------
# Convert integer labels back to string labels using the label encoder
true_labels_str = le.inverse_transform(all_true)
pred_labels_str = le.inverse_transform(all_preds)

# Calculate overall Macro F1 (for confirmation)
macro_f1 = f1_score(all_true, all_preds, average='macro')
logger.info(f"\n--- Test Set Results ---")
logger.info(f"Overall Macro F1 Score: {macro_f1:.4f}")

