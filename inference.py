SAMPLE_RATE = 22050
N_MELS = 128
N_FFT = 2048
HOP_LENGTH = 512
FMAX = SAMPLE_RATE // 2
TARGET_FRAMES = 1300
DURATION = TARGET_FRAMES * HOP_LENGTH / SAMPLE_RATE

DEVICE = torch.device("cuda" if torch.cuda.is_available() else "cpu")

# Paths
MODEL_PATH = CHECKPOINT_DIR / "best_model.pth"
